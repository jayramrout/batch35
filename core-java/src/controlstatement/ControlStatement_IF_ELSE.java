package controlstatement;

public class ControlStatement_IF_ELSE {
    public static void main(String[] args) {
        int age = 50;

        if (age == 10) {
            System.out.println("Age is 10");
        }else if (age == 30) {
            System.out.println("Age is Thirty");
        }else if (age == 50) {
            System.out.println("Age is Fifty");
        }else if (age == 100) {
            System.out.println("Age is hundred");
        }
    }
}
